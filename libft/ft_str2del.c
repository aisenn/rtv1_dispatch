#include "libft.h"

int	ft_str2del(char ***s2d)
{
	char	**str;
	int		i;

	i = 0;
	str = *s2d;
	while (*str)
	{
		ft_strdel(str);
		i++;
		str = &(*s2d)[i];
	}
	ft_memdel((void **)s2d);
	return (0);
}
